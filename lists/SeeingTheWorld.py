places_to_visit = ['Los Angels', 'Paris', 'Greece', 'Japan', 'Dubai']

print(places_to_visit)

places_to_visit_sorted = sorted(places_to_visit)

print(f"Temporary sorted list {places_to_visit_sorted}" )
print(f"Original List {places_to_visit}")

places_to_visit_sorted.reverse()
print(places_to_visit_sorted)
print(f"List in sorted reverse alphabetical order {places_to_visit_sorted}")
print(f"Original List {places_to_visit}")

